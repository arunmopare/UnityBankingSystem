package com.model;

public class Beneficiary {
  String b_id; 
  String b_name;
  String from_account;
  String to_account;
  String to_ifsc;
  String b_limit;
public String getB_id() {
	return b_id;
}
public void setB_id(String b_id) {
	this.b_id = b_id;
}
public String getB_name() {
	return b_name;
}
public void setB_name(String b_name) {
	this.b_name = b_name;
}
public String getFrom_account() {
	return from_account;
}
public void setFrom_account(String from_account) {
	this.from_account = from_account;
}
public String getTo_account() {
	return to_account;
}
public void setTo_account(String to_account) {
	this.to_account = to_account;
}
public String getTo_ifsc() {
	return to_ifsc;
}
public void setTo_ifsc(String to_ifsc) {
	this.to_ifsc = to_ifsc;
}
public String getB_limit() {
	return b_limit;
}
public void setB_limit(String b_limit) {
	this.b_limit = b_limit;
}
  
}
